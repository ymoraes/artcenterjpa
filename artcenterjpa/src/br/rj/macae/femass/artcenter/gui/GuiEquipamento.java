    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if(tblEquipamentos.getSelectedRow()>=0){
            EquipamentoControle controle = new EquipamentoControle();
            Object valor=((DefaultTableModel)tblEquipamentos.getModel()).getValueAt(tblEquipamentos.getSelectedRow(), 0);
            try {
                Equipamento c = controle.getEquipamentoPorId((Integer) valor);
                txtPart.setText(c.getPart());
                txtDescricao.setText(c.getDescricao());
                txtTipo.setText(c.getTipo());
                txtSerie.setText(c.getSerie());
                cboCategoria.setSelectedItem(c.getCategoria());
                
                habilitarCampos();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma linha da tabela.");
        }

    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(tblEquipamentos.getSelectedRow()>=0){
            EquipamentoControle controle = new EquipamentoControle();
            Object valor=((DefaultTableModel)tblEquipamentos.getModel()).getValueAt(tblEquipamentos.getSelectedRow(), 0);
            try {
                Equipamento c = controle.getEquipamentoPorId((Integer)valor);
                controle.excluir(c);
                
                desabilitarCampos();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma linha da tabela.");
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        EquipamentoControle controle = new EquipamentoControle();

        try {
            Equipamento c = new Equipamento();
            c.setPart(txtPart.getText());
            c.setDescricao(txtDescricao.getText());
            c.setTipo(txtTipo.getText());
            c.setSerie(txtSerie.getText());
            c.setCategoria((Categoria) cboCategoria.getSelectedItem());
            if(tblEquipamentos.getSelectedRow()>=0){
            c.setId((Integer) tblEquipamentos.getValueAt(tblEquipamentos.getSelectedRow(), 0));
        }
        
            controle.gravar(c);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }catch(Exception ex2){
            JOptionPane.showMessageDialog(this, ex2.getMessage());
        }
        
        limparCampos();
        desabilitarCampos();
        atualizarLista();
    }//GEN-LAST:event_btnGravarActionPerformed

