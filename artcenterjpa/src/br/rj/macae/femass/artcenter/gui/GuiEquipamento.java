 private void limparCampos() {
        txtPart.setText("");
        txtDescricao.setText("");
        txtTipo.setText("");
        txtSerie.setText("");
        tblEquipamentos.clearSelection();   
        try {
            atualizarComboCategoria();
            cboCategoria.setSelectedIndex(-1);
        } catch (SQLException ex) {
            Logger.getLogger(GuiEquipamento.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
    
    }

    private void habilitarCampos() {
        pNovo.setVisible(true);
        txtPart.requestFocus(true);
        btnGravar.setVisible(true);     
        btnCancelar.setVisible(true); 
        tblEquipamentos.setEnabled(false);
    }
    private void desabilitarCampos() {
        pNovo.setVisible(false);
        btnGravar.setVisible(false);     
        btnCancelar.setVisible(false);     
        tblEquipamentos.setEnabled(true);
        
    }
    
    private void atualizarLista(){
           EquipamentoControle controle = new EquipamentoControle();
        try {
            controle.atualizarLista(tblEquipamentos);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }        
    }
private void atualizarComboCategoria() throws SQLException {
        EquipamentoControle controle = new EquipamentoControle();
        List categorias = controle.listarCategorias();
        cboCategoria.removeAllItems();
        for(Object o:categorias){
            cboCategoria.addItem(o);
        }
    }
